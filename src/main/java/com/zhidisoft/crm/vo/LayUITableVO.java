package com.zhidisoft.crm.vo;

public class LayUITableVO {

	private Integer code;
	private String msg;
	private Long count;
	private Object data;
	public Integer getCode() {
		return code;
	}
	public LayUITableVO setCode(Integer code) {
		this.code = code;
		return this;
	}
	public String getMsg() {
		return msg;
	}
	public LayUITableVO setMsg(String msg) {
		this.msg = msg;
		return this;
	}
	public Long getCount() {
		return count;
	}
	public LayUITableVO setCount(Long count) {
		this.count = count;
		return this;
	}
	public Object getData() {
		return data;
	}
	public LayUITableVO setData(Object data) {
		this.data = data;
		return this;
	}
	
	
	
}
